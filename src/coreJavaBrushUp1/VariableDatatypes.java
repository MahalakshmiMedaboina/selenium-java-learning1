package coreJavaBrushUp1;

public class VariableDatatypes {

	public static void main(String[] args) {
		int myNum = 5; // integer (whole number) primitive data type
		String website = "Rahul Shetty Academy"; // non-primitive data type
		char letter = 'r'; // character primitive data type
		float myFloatNum = 5.99f; // floating point number primitive data type
		double myDoubleNum = 19.99d; // double floating point number primitive data type
		boolean isJavaFun = true; // boolean primitive data type
		
		System.out.println(myNum + "is the value of myNum variable");
		System.out.println(website);
		System.out.println(letter);
		System.out.println(myFloatNum);
		System.out.println(myDoubleNum);
		System.out.println(isJavaFun);
	}

}
